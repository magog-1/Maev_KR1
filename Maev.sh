#!/bin/bash

# Задание 3.15
# Скрипт Maev.sh

# 1. Создаём новый скрипт testfile.sh
cat > testfile.sh << 'EOF'
#!/bin/bash
echo "testfile.sh создан скриптом Maev.sh"
EOF
chmod +x testfile.sh
echo "Скрипт testfile.sh создан."

# 2. Вывод списка всех файлов в текущем рабочем каталоге в формате длинного списка
echo ""
echo "=== Список файлов в текущем каталоге (длинный формат) ==="
ls -la

# 3. Вывод системной информации и версии выпуска ядра
echo ""
echo "=== Системная информация и версия ядра ==="
uname -a

# 4. Изменение текущего каталога на tasks/templates
echo ""
echo "=== Переход в каталог tasks/templates ==="
cd tasks/templates
echo "Текущий каталог: $(pwd)"

# 5. Вывод имени текущего пользователя, вошедшего в терминал
echo ""
echo "=== Текущий пользователь ==="
whoami

# 6. Сохранение данных с главной страницы Финансового университета в файл Maev.txt
echo ""
echo "=== Сохранение главной страницы Финансового университета ==="
curl -s https://www.fa.ru/ -o ../../Maev.txt
echo "Данные сохранены в файл Maev.txt"
